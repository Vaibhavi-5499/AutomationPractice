package Variable;

public class Variable2
{
    public static void main(String args[])
    {
    	//Value of variable can be changed as per requirement
    	
    	int age=10;
    	System.out.println("Value of age is:\t"+age);
    	
    	age =20;
    	System.out.println("First Updated value of age is:\t "+age);
    	
    	age =30;
    	System.out.println("Second time updated value of age is\t"+age);
    	
    
    }
}

/*
 
Definition: Variable is used to store/hold value at memory location, Value of the variable can be changed as per the
requirement in program.

Variable 1.----> points to the memory location--->data ---> can be change based on requirement --->Variable
		 2.----> is a container --> which holds the value ---> while writing the program
		 3.----> is a container or memory location---> which holds the value ---> value can be change based on requirement ---> Variable
*/