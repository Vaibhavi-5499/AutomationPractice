package Variable;

class Variable5
{
     public static void main(String args[])
    {
	
    	 int Num1=100,Num2=200,result=0;
    	 
    	 result=Num1+Num2;
    	 
    	 System.out.println("Value of Num1="+Num1);
    	 System.out.println("Value of Num2="+Num2);
    	 System.out.println("Addition of Num1 and Num2 is:"+result);
    	 
    	 System.out.println("--------------------------------------------");
    	 
    	 //Writing all above 3 statement in single statement using \n(for new line)
    	 
    	 System.out.println("Value of Num1="+Num1+"\nValue of Num2="+Num2+"\nAddition of Num1 and Num2 is:"+result);
    	 
    	 
    	 //use of /t(for printing space in between)
    	 System.out.println("Value of Num1="+Num1+"\tValue of Num2="+Num2+"\tAddition of Num1 and Num2 is:"+result);
    	  
    }
}
