package Variable;

public class LocalVariable {

	public static void main(String[] args) {
		
		//Local Variable
		
		int age=25;
		
		System.out.println("Local Variable : Age ="+age);

	}

}

/*
 * Local Variable: A Variable declared inside method body or passed as an argument is known as local variable.
  
   Local Variable must be initialized before we access it, Otherwise java compiler will give error.
 * */
