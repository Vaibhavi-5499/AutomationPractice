package simpleprintprogram;

public class Demo1 {

	public static void main(String[] args) {
	
		System.out.println("Program starts here");
		System.out.println("Program ends here");
		System.out.println("@#$%^&*(1234567890");
		
		//print and println difference 
		
		
		 
		System.out.println("Hello"); //New line will start after this statement 
		System.out.print("My name is Vaibhavi,");    //new line will not start after this statement
		System.out.println("My surname is Shinde"); //new line will start  after this statement
		System.out.println("Bye"); //new line will start after this statement  
		
		
		/*
		 1.print()  :New line will not be printed after execution of this statement
		 2.println():New line will be printed after execution of this statement
		 */

	}

}
